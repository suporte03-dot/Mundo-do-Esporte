export const DEFAULT_SAFETY_TIPS = [
  'Priorize execução correta.',
  'Evite cargas excessivas.',
  'Controle o movimento em todas as fases.',
  'Pare em caso de dor.',
  'Este conteúdo é informativo e não substitui orientação de um profissional. Em caso de dor, lesão ou desconforto, interrompa o exercício e procure orientação especializada.',
]

function createExercise({
  id,
  name,
  type,
  category,
  mediaType = 'image',
  muscles,
  benefits,
  execution,
  commonMistakes,
  safetyTips,
  sets,
  reps,
  rest,
  equipment,
  level,
  restrictions,
  video,
  gif,
  image,
}) {
  const media = resolveExerciseMedia(id, category, type)
  const resolvedVideo = video ?? null
  const resolvedGif = gif ?? media.gif ?? null
  const primaryImage = image || media.image || null
  const fallbackImage = media.fallbackImage

  // Confiar em resolveExerciseMedia (inclui mídia local de Peito) — não sobrescrever com GIF remoto
  const hasOverride = Boolean(resolvedVideo || resolvedGif || primaryImage)
  const pending = Boolean(media.mediaPending && !hasOverride)

  let resolvedMediaType = 'image'
  let resolvedMediaUrl = fallbackImage

  if (resolvedVideo) {
    resolvedMediaType = 'video'
    resolvedMediaUrl = resolvedVideo
  } else if (resolvedGif) {
    resolvedMediaType = 'gif'
    resolvedMediaUrl = resolvedGif
  } else if (primaryImage) {
    resolvedMediaType = 'image'
    resolvedMediaUrl = primaryImage
  } else if (!pending && media.mediaUrl) {
    resolvedMediaType = media.mediaType || 'image'
    resolvedMediaUrl = media.mediaUrl
  }

  const displayGroup = normalizeMuscleGroup(category)

  return {
    id,
    name,
    type,
    category: displayGroup,
    muscleGroup: displayGroup,
    secondaryMuscles: muscles?.slice(1) ?? [],
    mediaType: pending ? 'image' : resolvedMediaType,
    mediaUrl: pending ? fallbackImage : resolvedMediaUrl,
    image: pending ? null : primaryImage || (resolvedMediaType === 'image' ? resolvedMediaUrl : null),
    thumbnail: pending ? fallbackImage : media.thumbnail || resolvedMediaUrl,
    gif: pending ? null : resolvedGif,
    video: resolvedVideo,
    fallbackImage,
    fallbackSvg: media.fallbackSvg,
    poseKey: media.poseKey,
    mediaPending: pending,
    hasVerifiedMedia: !pending && Boolean(resolvedMediaUrl && resolvedMediaUrl !== fallbackImage),
    shortInstruction: execution?.[0] ?? '',
    executionSteps: execution,
    muscles: muscles?.map((m) => (m === 'Peito' ? 'Peitoral' : m === 'Peito superior' ? 'Peitoral superior' : m)),
    benefits,
    execution,
    commonMistakes,
    safetyTips: safetyTips ?? DEFAULT_SAFETY_TIPS,
    sets,
    reps,
    rest,
    equipment,
    level,
    ...(restrictions ? { restrictions } : {}),
  }
}

