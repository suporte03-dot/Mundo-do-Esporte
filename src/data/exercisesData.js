export function parseSets(sets) {
  const nums = String(sets).match(/\d+/g)
  if (!nums?.length) return 3
  return Math.max(...nums.map(Number))
}

export function parseRestSeconds(rest) {
  if (!rest || rest === '—') return 0
  const nums = String(rest).match(/\d+/g)
  if (!nums?.length) return 60
  return Math.max(...nums.map(Number))
}

export function getExerciseMuscleGroup(exercise) {
  return exercise?.category || exercise?.muscleGroup || 'Outros'
}

export function getExerciseById(id) {
  const fromCache = getExerciseFromCache(id)
  if (fromCache) return fromCache
  return exercises.find((e) => e.id === id) || null
}

export function getExercisesByIds(ids = []) {
  return ids.map((id) => getExerciseById(id)).filter(Boolean)
}
